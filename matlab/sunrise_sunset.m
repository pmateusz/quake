% London sunrise and sunset times at given dates during the year

London.date = [1 1; 1 15; 1 30; 2 15; 2 28; 3 15; 3 30; 4 15; 4 30; ...
    5 15; 5 30; 6 15; 6 30; 7 15; 7 30; 8 15; 8 30; 9 15; 9 30; 10 15; ...
    10 30; 11 15; 11 30; 12 15; 12 31];

London.sunrise = [8 06; 7 59; 7 41; 7 14; 6 48; 6 14; 5 40; 5 05; 4 34; ...
    04 08; 03 50; 3 32; 3 46; 4 00; 04 21; 4 45; 05 09; 05 35; 05 59; 06 24; ...
    06 50; 07 18; 07 42; 07 56; 08 06];

London.sunset = [16 02; 16 20; 16 46; 17 15; 17 38; 18 04; 18 30; 18 57; 19 21; ...
    19 45; 20 05; 20 19; 20 21; 20 11; 19 51; 19 23; 18 51; 18 15; 17 40; 17 07; ...
    16 37; 16 11; 15 55; 15 51; 16 00];

for i = 1 : size(London.date,1)
    London.elapsed_days(i) = date2jd([2030 London.date(i,1) London.date(i,2) 0 0 0]) - ...
        date2jd([2030 1 1 0 0 0]);
end

for i = 1 : size(London.date,1)
    London.sunrise_elapsed_sec(i) = ...
        (date2jd([2030 London.date(i,1) London.date(i,2) London.sunrise(i,1) London.sunrise(i,2) 0]) - ...
        date2jd([2030 London.date(i,1) London.date(i,2) 0 0 0])) * 86400;
end

for i = 1 : size(London.date,1)
    London.sunset_elapsed_sec(i) = ...
        (date2jd([2030 London.date(i,1) London.date(i,2) London.sunset(i,1) London.sunset(i,2) 0]) - ...
        date2jd([2030 London.date(i,1) London.date(i,2) 0 0 0])) * 86400;
end

